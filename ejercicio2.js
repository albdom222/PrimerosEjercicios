let numero1 = parseFloat(prompt("Introduce el primer número:"));
let numero2 = parseFloat(prompt("Introduce el segundo número:"));

// Calcula y muestra la suma
let suma = numero1 + numero2;
console.log("La suma de los dos números es: " +  suma);

// Extra: operaciones adicionales
console.log("La resta es: " + (numero1 - numero2));
console.log("La multiplicación es: " + (numero1 * numero2));
console.log("La división es: " + (numero1 / numero2));